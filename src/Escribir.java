public class Escribir {
    public static void main(String[] args) {
        // Mensajes que se mostrarán en la consola
        System.out.println("Hola, este es un ejemplo de escritura en consola.");
        System.out.println("Puedes mostrar diferentes tipos de datos:");

        int numero = 25;
        double decimal = 3.14;
        String texto = "Esto es una cadena de texto";

        // Mostrar variables en la consola
        System.out.println("Número entero: " + numero);
        System.out.println("Número decimal: " + decimal);
        System.out.println("Texto: " + texto);
    }
}
